<p class="glsr-heading">rating</p>
<p>Include the "rating" option to display images of reviews which have a specific <em>minimum</em> rating. By default, the shortcode displays images of 1-5 star reviews.</p>
<p>The default rating value is: <code>"1"</code></p>
<div class="shortcode-example">
    <input type="text" readonly class="code" value='[site_reviews_images rating="4"]'>
    <pre><code class="syntax-shortcode"><span class="tag">[site_reviews_images</span> <span class="attr-name">rating</span>=<span class="attr-value">"4"</span><span class="tag">]</span></code></pre>
</div>
